package com.allcorp.routeconfigurer.bean;

/**
 * @author Aashi
 *
 */
public class RouteBean {
   
	int routeId;
	double originAddress;
	double destinationAddress;
	double latitude;
	double longitude;
	double distance;
	double time;
	/**
	 * @return the routeId
	 */
	public int getRouteId() {
		return routeId;
	}
	/**
	 * @param routeId the routeId to set
	 */
	public void setRouteId(int routeId) {
		this.routeId = routeId;
	}
	/**
	 * @return the originAddress
	 */
	public double getOriginAddress() {
		return originAddress;
	}
	/**
	 * @param originAddress the originAddress to set
	 */
	public void setOriginAddress(double originAddress) {
		this.originAddress = originAddress;
	}
	/**
	 * @return the destinationAddress
	 */
	public double getDestinationAddress() {
		return destinationAddress;
	}
	/**
	 * @param destinationAddress the destinationAddress to set
	 */
	public void setDestinationAddress(double destinationAddress) {
		this.destinationAddress = destinationAddress;
	}
	/**
	 * @return the latitude
	 */
	public double getLatitude() {
		return latitude;
	}
	/**
	 * @param latitude the latitude to set
	 */
	public void setLatitude(double latitude) {
		this.latitude = latitude;
	}
	/**
	 * @return the longitude
	 */
	public double getLongitude() {
		return longitude;
	}
	/**
	 * @param longitude the longitude to set
	 */
	public void setLongitude(double longitude) {
		this.longitude = longitude;
	}
	/**
	 * @return the distance
	 */
	public double getDistance() {
		return distance;
	}
	/**
	 * @param distance the distance to set
	 */
	public void setDistance(double distance) {
		this.distance = distance;
	}
	/**
	 * @return the time
	 */
	public double getTime() {
		return time;
	}
	/**
	 * @param time the time to set
	 */
	public void setTime(double time) {
		this.time = time;
	}
	
	
}
